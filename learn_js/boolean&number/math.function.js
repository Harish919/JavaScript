//1. Math.random() --> Generates a random number between 0 and 1
const a = Math.random();
console.log("random",a);

//2.Math.floor() --> Rounds a number down
const b = Math.floor(58.76564965267961)
console.log("floor:",b);

//3.Math.celi() --> Rounds a number up
const c = Math.ceil(24.1);
console.log("celi:",c);

//4.Math.round() --> Round to the nearest integer
const d = Math.round(17.4);
console.log("round:",d);
const e = Math.round(17.5)
console.log("round:",e);

//5.Math.max() --> Return the largest number
const f = Math.max(10,20,24,17,67,19);
console.log("max:",f);

//6.math.min() --> Returns the smallest  number
const g = Math.min(17,20,24,89,67);
console.log("min:",g);

//7.math.pow() --> power(exponent)
const h = Math.pow(2,3);
console.log(`power:`,h);

//8.math.sqrt() --> Square root
const i = Math.sqrt(49);
console.log("square_Root:",i);

//9.math.trunc () -->removes the whole number and gives the decimal point

const j =Math.trunc(2.99);
console.log(j);
